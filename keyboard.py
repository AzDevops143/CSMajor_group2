KEYBOARD_LAYOUT = dict(KEYBOARD_LAYOUTS['qwerty'])
ACTIVE_LAYOUT_NAME = 'qwerty'

def set_active_layout(name):
    """Updates KEYBOARD_LAYOUT in-place."""
    global ACTIVE_LAYOUT_NAME
    if name in KEYBOARD_LAYOUTS:
        KEYBOARD_LAYOUT.clear()
        KEYBOARD_LAYOUT.update(KEYBOARD_LAYOUTS[name])
        ACTIVE_LAYOUT_NAME = name
        return True
    return False

def get_key_from_pos(x, y, threshold=0.6):
    """Finds the closest key to a given (x, y) coordinate."""
    closest_key = None
    min_dist = float('inf')
    for key, (kx, ky) in KEYBOARD_LAYOUT.items():
        dist = ((kx - x)**2 + (ky - y)**2)**0.5
        if dist < min_dist:
            min_dist = dist
            closest_key = key
    return closest_key if min_dist <= threshold else None
